function radiansToDegrees(rad) {
	return (rad * 180)/Math.PI
}

console.log(radiansToDegrees(1), 57.29577951308232)
console.log(radiansToDegrees(5), 286.4788975654116)
console.log(radiansToDegrees(7), 401.07045659157626)
console.log(radiansToDegrees(100), 5729.5779513082325)
console.log(radiansToDegrees(180), 10313.240312354817)
